public class Curso {

    private int id_curso;
	private int id_sede;
	private String nombre_curso;
	private int horario;
	private int duración; 
	private String dias; 
	private int cantidad_estudiantes;
	private String salon_asignado;
	private Boolean asignado;
	private String edificio;

	
	public int getId_curso() {
		return id_curso;
	}
	public void setId_curso(int id_curso) {
		this.id_curso = id_curso;
	}
	public int getId_sede() {
		return id_sede;
	}
	public void setId_sede(int id_sede) {
		this.id_sede = id_sede;
	}
	public String getNombre_curso() {
		return nombre_curso;
	}
	public void setNombre_curso(String nombre_curso) {
		this.nombre_curso = nombre_curso;
	}
	public int getHorario() {
		return horario;
	}
	public void setHorario(int horario) {
		this.horario = horario;
	}
	public int getDuración() {
		return duración;
	}
	public void setDuración(int duración) {
		this.duración = duración;
	}
	public String getDias() {
		return dias;
	}
	public void setDias(String dias) {
		this.dias = dias;
	}
	public int getCantidad_estudiantes() {
		return cantidad_estudiantes;
	}
	public void setCantidad_estudiantes(int cantidad_estudiantes) {
		this.cantidad_estudiantes = cantidad_estudiantes;
	}
	public String getSalón_asignado() {
		return salon_asignado;
	}
	public void setSalón_asignado(String salón_asignado) {
		this.salon_asignado = salón_asignado;
	}
	public Boolean getAsignado() {
		return asignado;
	}
	public void setAsignado(Boolean asignado) {
		this.asignado = asignado;
	}
	public String getEdificio() {
		return edificio;
	}
	public void setEdificio(String edificio) {
		this.edificio = edificio;
	}





	
}
